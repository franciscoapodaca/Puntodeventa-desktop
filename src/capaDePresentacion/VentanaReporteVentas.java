    private void botonReportePorFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonReportePorFechaActionPerformed
        if (campoFechaInicio.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "VERIFIQUE NO EXISTAN CAMPOS VACIOS");
        } else {
            try {
                String ruta = "src/reportes/reporteVentasTotalesPorFecha.jasper";
                Map parametros = new HashMap();
                parametros.put("FECHAV", campoFechaInicio.getText());
                JasperPrint reporte = JasperFillManager.fillReport(ruta, parametros, facade.GetConnection());
                JasperExportManager.exportReportToPdfFile(reporte, "src/reportes/reporteVentasTotalesPorFecha.pdf");
//            JasperViewer ventana = new JasperViewer(reporte, false);
//            ventana.setTitle("REPORTE DE INGREDIENTES");
//            ventana.setVisible(true);
//            JOptionPane.showMessageDialog(null, "Reporte Generado en formato PDF");
                try {
                    File path = new File("src/reportes/reporteVentasTotalesPorFecha.pdf");
                    Desktop.getDesktop().open(path);
                } catch (IOException ex) {
                    ex.printStackTrace();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Inserte una Fecha Desde");
            }
        }
    }//GEN-LAST:event_botonReportePorFechaActionPerformed

