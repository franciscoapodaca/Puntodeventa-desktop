    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        if (txtNombre.getText().trim().length() == 0 || txtExistencia.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "VERIFIQUE NO EXISTAN CAMPOS VACIOS");
        } else {
            beb.setNombre(txtNombre.getText());
            beb.setId(Integer.parseInt(txtId.getText()));
            beb.setExistencia(Double.parseDouble(txtExistencia.getText()));
            beb.setPrecio(Double.parseDouble(txtPrecio.getText()));
            txtId.setEnabled(false);
            try {
                admin.actualizarBebida(beb);
                JOptionPane.showMessageDialog(null, "Bebida Actualizada");
                botonAgregar.setEnabled(true);
                mostrarDatos();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR EL INGREDIENTE ", "¡ERROR!", 0);
                
            }
        }
    }//GEN-LAST:event_botonActualizarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        if (txtId.getText().trim().length() == 0 || txtNombre.getText().trim().length() == 0 || txtExistencia.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "VERIFIQUE QUE NO EXISTAN CAMPOS VACIOS");
        } else {
            beb.setId(Integer.parseInt(txtId.getText()));
            try {
                admin.eliminarBebida(beb);
                JOptionPane.showMessageDialog(null, "Bebida Eliminada");
                txtId.setText(null);
                txtNombre.setText(null);
                txtExistencia.setText(null);
                txtNombreBuscar.setText(null);
                mostrarDatos();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "ERROR AL ELIMINAR LA BEBIDA ", "¡ERROR!", 0);
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

