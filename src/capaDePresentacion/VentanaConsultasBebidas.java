    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        if (txtNombreBuscar.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "VERIFIQUE QUE NO EXISTAN CAMPOS VACIOS");
        } else {
            DefaultTableModel dtm = new DefaultTableModel();
            this.tablaBebidas.setModel(dtm);
            ResultSet rs = null;
            dtm.setColumnIdentifiers(new Object[]{"ID", "NOMBRE DE BEBIDA", "PRECIO", "EXISTENCIA EN PIEZAS"});
            facade.GetConnection();
            rs = admin.BuscarLike(txtNombreBuscar.getText());
            txtNombreBuscar.setText(null);
            try {
                while (rs.next()) {
                    dtm.addRow(new Object[]{rs.getInt("id_bebida"), rs.getString("nombre"), rs.getFloat("precio"), rs.getFloat("existencia")});
                }
            } catch (SQLException ex) {
                Logger.getLogger(VentanaConsultasBebidas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        

    }//GEN-LAST:event_botonBuscarActionPerformed

