    public void mostrarDatos() {
        DefaultTableModel dtm = new DefaultTableModel();
        this.tablaBebidas.setModel(dtm);
        ResultSet rs = null;
        dtm.setColumnIdentifiers(new Object[]{"ID", "NOMBRE DE BEBIDA", "PRECIO", "EXISTENCIA EN PIEZAS"});
        facade.GetConnection();
        rs = admin.obtenerTodasBebidas();
        try {
            while (rs.next()) {
                dtm.addRow(new Object[]{rs.getInt("id_bebida"), rs.getString("nombre"), rs.getFloat("precio"), rs.getInt("existencia")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(VentanaConsultasBebidas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

